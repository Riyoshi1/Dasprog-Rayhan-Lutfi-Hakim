jml_barang = int(input("Masukkan jumlah barang: "))
stok = []

for i in range(jml_barang):
    barang = input("Barang dan Jumlah barang: ").split()
    nama_barang = barang[0]
    jumlah_barang = int(barang[1])
    stok.append([nama_barang, jumlah_barang])

beli = int(input("Masukkan jumlah pembelian: "))
for _ in range(beli):
    beli_barang = input("Barang dan Jumlah barang yang dibeli: ").split()
    nama_beli = beli_barang[0]
    jumlah_beli = int(beli_barang[1])

    for j in range(len(stok)):
        if stok[j][0] == nama_beli:
            stok[j][1] -= jumlah_beli

for item in stok:
    print(f'{item[0]}:{item[1]}')

