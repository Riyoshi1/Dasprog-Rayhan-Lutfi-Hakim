# MS
jml_sepatu = int(input("Masukkan jumlah sepatu: "))
kanan = []
kiri = []

for i in range(jml_sepatu):
    kode_anomali = input('').strip()

    sisi = ''
    for bagian in kode_anomali:
        if bagian.isupper():#untuk bagian sepatu kiri atau kanan
            sisi = bagian

    kode_angka = []
    for angka in kode_anomali: #untuk ukuran sepatu
        if angka.isdigit():
            kode_angka.append(angka)

    ukuran = ''.join(kode_angka)

    if sisi == 'L':
        kiri.append(ukuran)
    else:
        kanan.append(ukuran)

berpasangan = 0
for sepasang in kiri[:]: #agar saat angka yang ditentukan dihapus, akan tetap terprint
    if sepasang in kanan:
        berpasangan += 1
        kanan.remove(sepasang) #menghapus ukuran sepatu yang sudah berpasangan agar tidak dihitung lagi
        kiri.remove(sepasang)
print(berpasangan)

# Djumanto kebingungan
N = int(input())
kalimat = []
for _ in range(N):
  kode = int(input())
  biner = bin(kode)[2:] #0bbiner agar 0b dihilangkan
  angka = biner.zfill(32)
  string = str(angka)
  for kata in range(0, len(string), 8):
    huruf = string[kata:kata + 8]
    bit = int(huruf, 2)
    kalimat.append(bit)

print(''.join(chr(huruf) for huruf in kalimat))

